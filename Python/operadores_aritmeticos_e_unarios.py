
numero1 = 10
numero2 = 3

"""Operadores binários"""
resultado = numero1 - numero2 #subtração
print (resultado)

resultado = numero1 + numero2 #soma
print (resultado)

resultado = numero1 * numero2 #multiplicação
print (resultado)

resultado = numero1 / numero2 #divisão
print (resultado)

resultado = numero1 ** numero2 #exponenciação
print (resultado)

resultado = numero1 % numero2 #modulo
print (resultado)


"""Operadores unários"""
print("-------------------------------------")

valor = 10
print(valor)

#valor++ não existe em python
valor += 1
print(valor)

valor -= 1
print(valor)

valor /= 2
print(valor)

valor *= 2
print(valor)

